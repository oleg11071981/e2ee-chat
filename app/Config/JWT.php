<?php

namespace App\Config;

/**
 * Конфигурация JWT (JSON Web Token)
 *
 * Этот класс отвечает за настройки JWT аутентификации в приложении.
 * Содержит параметры для генерации и проверки JWT токенов.
 */
class JWT
{
    /**
     * Секретный ключ для подписи токенов
     *
     * @var string|array|false Секретный ключ в виде строки или false если не найден
     *
     * Может быть:
     * - строкой (string) - сам ключ
     * - массивом (array) - для разных алгоритмов
     * - false - если ключ не найден в .env
     */
    public string|array|false $key;

    /**
     * Алгоритм шифрования JWT
     *
     * @var string Алгоритм (по умолчанию HS256)
     *
     * Поддерживаемые алгоритмы:
     * - HS256 (HMAC with SHA-256) - симметричный
     * - HS384 (HMAC with SHA-384) - симметричный
     * - HS512 (HMAC with SHA-512) - симметричный
     * - RS256 (RSA with SHA-256) - асимметричный
     */
    public string $algorithm = 'HS256';

    /**
     * Время жизни токена в секундах
     *
     * @var int Время в секундах (по умолчанию 604800 - 7 дней)
     *
     * Примеры:
     * - 3600 - 1 час
     * - 86400 - 1 день
     * - 604800 - 7 дней (неделя)
     * - 2592000 - 30 дней
     */
    public int $expiration = 604800; // 7 дней

    /**
     * Конструктор класса JWT
     *
     * При создании объекта автоматически загружает секретный ключ
     * из переменной окружения JWT_SECRET.
     *
     * Порядок загрузки ключа:
     * 1. Пытается получить ключ из getenv('JWT_SECRET')
     * 2. Если ключ не найден, использует резервный ключ по умолчанию
     *
     * Рекомендации:
     * - В production всегда используйте переменную окружения
     * - Никогда не храните реальные ключи в коде
     * - Регулярно меняйте секретные ключи
     */
    public function __construct()
    {
        $this->key = getenv('JWT_SECRET');
    }
}